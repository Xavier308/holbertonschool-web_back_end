#!/usr/bin/env python3
"""
This module contains a type-annotated function
to convert a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string form.

    Args:
        n (float): The float number to be converted.

    Returns:
        str: The string form of the float.
    """
    return str(n)
