#!/usr/bin/env python3
"""
This module contains a type-annotated function to concatenate two strings.
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the combined result.

    Args:
        str1 (str): The first string to join.
        str2 (str): The second string to join.

    Returns:
        str: The combined string.
    """
    return str1 + str2
